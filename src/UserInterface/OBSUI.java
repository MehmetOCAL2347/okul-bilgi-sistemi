                        jLabel_OgretmenIsmi_3.setText(atanmısOgretmenler.get(i).getIsim().toUpperCase());
                        jLabel_OgretmenSoyisim_3.setText(atanmısOgretmenler.get(i).getSoyIsım().toUpperCase());
