    public void kullanıcıRoleBelirle(Kullanıcı kullanıcı){
    
        switch (kullanıcı.getRole()) {
            case "Admin":
                admin = (Admin) kullanıcı;
                break;
            case "Ogrenci":
                ogrenci = (Ogrenci) kullanıcı;
                break;
            case "Ogretmen":
                ogretmen = (Ogretmen) kullanıcı;
                break;
            case "OkulMuduru":
                okulMuduru = (OkulMuduru) kullanıcı;
                break;
            default:
                kullanıcı = null;
                break;
        }
        
    }
        
